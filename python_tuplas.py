"""
tipo tupla - Uma lista imutável
"""

nomes = 'Maria','Helena','Luiz' #CRIAÇÃO DE TUPLAS
nomes_0 = ('Maria','Helena','Luiz') #outro metodo de criar uma tupla
print(nomes[0])

#conversão de lista para Tupla.
nomes_1 = ['Maria','Helena','Luiz']
nomes_1 = tuple(nomes_1) #convertendo lista para tupla)
